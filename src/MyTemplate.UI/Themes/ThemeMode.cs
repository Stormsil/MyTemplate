    /// </summary>
    Light,

    /// <summary>
    ///     The dark theme.
    /// </summary>
    Dark,

    /// <summary>
    ///     The green light theme.
    /// </summary>
    GreenLight,

    /// <summary>
    ///     The green dark theme.
    /// </summary>
    GreenDark,

    /// <summary>
    ///     The yellow light theme.
    /// </summary>
    YellowLight,

    /// <summary>
    ///     The yellow dark theme.
    /// </summary>
    YellowDark,

    /// <summary>
    ///     The orange light theme.
    /// </summary>
    OrangeLight,

    /// <summary>
    ///     The orange dark theme.
    /// </summary>
    OrangeDark,

    /// <summary>
    ///     The violet light theme.
    /// </summary>
    VioletLight,

    /// <summary>
    ///     The violet dark theme.
    /// </summary>
    VioletDark
}